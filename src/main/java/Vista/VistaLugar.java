    private void butBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butBuscarActionPerformed
        String buscarLugar = txtBuscarLugar.getText();
        int codLugar = 0;

        if (buscarLugar.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el codigo del lugar", "Atencion", JOptionPane.ERROR_MESSAGE);
            return;
        }
        try {
            codLugar = Integer.parseInt(buscarLugar);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Tipo de dato no valido", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Lugar lugar = null;
        lugar = lugarData.buscarLugar(codLugar);

        if (lugar == null) {
            JOptionPane.showMessageDialog(this, "No se encontro el lugar con ese id", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        DefaultTableModel modelo = (DefaultTableModel) TablaLugares.getModel();
        modelo.setRowCount(0);
        Object[] fila = {lugar.getCodLugar(), lugar.getFila(), lugar.getNumero(), lugar.isEstado(), lugar.getFuncion().getCodFuncion()};
        modelo.addRow(fila);
    }//GEN-LAST:event_butBuscarActionPerformed

