    private void cargarLugares() {
        List<Lugar> listaLugares = null;
        try {
            listaLugares = lugarData.listarLugares();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Error al cargar los lugares de la base de datos.",
                    "Error de Base de Datos",
                    JOptionPane.ERROR_MESSAGE);
        }

        DefaultTableModel modelo = (DefaultTableModel) TablaLugares.getModel();
        modelo.setRowCount(0);

        if (listaLugares == null) {
            JOptionPane.showMessageDialog(this,
                    "Error, no hay lugares cargados en la base de datos.",
                    "Error de Base de Datos",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        for (Lugar lugar : listaLugares) {
            Object[] fila = {lugar.getCodLugar(), lugar.getFila(), lugar.getNumero(), lugar.isEstado(), lugar.getFuncion().getCodFuncion()};
            modelo.addRow(fila);
        }
    }

