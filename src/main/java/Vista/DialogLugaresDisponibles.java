    public void cargarLugares() {
        lugarData.setFuncionData(funcionData);
        funcion.setLugaresDisponibles(lugarData.obtenerLugaresDisponiblesPorFuncion(funcion.getCodFuncion()));
        DefaultTableModel modelo = (DefaultTableModel) tablaLugares.getModel();
        modelo.setRowCount(0);

        for (Lugar l : funcion.getLugaresDisponibles()) {
            Object[] fila = {l.getCodLugar(),
                l.getFila(),
                l.getNumero(),
                l.isEstado() ? "Ocupado" : "Disponible",
                l.getFuncion().getCodFuncion()};
            modelo.addRow(fila);
        }
    }
    
    public void agregarLugar(Lugar nuevoLugar) {
    boolean existe = false;

    for (Lugar l : lugaresReservados) {
        if (l.getCodLugar() == nuevoLugar.getCodLugar()) {
            existe = true;
            break;
        }
    }

    if (!existe) {
        lugaresReservados.add(nuevoLugar);
         JOptionPane.showMessageDialog(this, "Lugar Reservado correctamente en la lista.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    } else {
      JOptionPane.showMessageDialog(this, "el lugar ya esta reservado en la lista", "Error", JOptionPane.ERROR_MESSAGE);
    }
}


