    private void butReservarAsientosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butReservarAsientosActionPerformed
        
         if (tablaLugares.isEditing()) {
            tablaLugares.getCellEditor().stopCellEditing();
        }
        int filaSeleccionada = tablaLugares.getSelectedRow();

        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una fila.");
            return;
        }

        int filaModelo = tablaLugares.convertRowIndexToModel(filaSeleccionada);

        String codLugar = tablaLugares.getModel().getValueAt(filaModelo, 0).toString();
        String fila = tablaLugares.getModel().getValueAt(filaModelo, 1).toString();
        String numero = tablaLugares.getModel().getValueAt(filaModelo, 2).toString();
        String estadoStr = tablaLugares.getModel().getValueAt(filaModelo, 3).toString();

        int codLugarInt = Integer.parseInt(codLugar);
        char filaLetra = fila.charAt(0);
        int numeroAsiento = Integer.parseInt(numero);

        boolean estado = false;
        if (estadoStr.equals("Ocupado")) {
            estado = true;
        }
        
      
        Lugar lugar = new Lugar(codLugarInt, filaLetra, numeroAsiento, estado, funcion);
        
          
          agregarLugar(lugar);
       
        cargarLugares();
    }//GEN-LAST:event_butReservarAsientosActionPerformed

